package com.example.basickotlin.Basic

fun main() {

    var character : Char  = 'A'
    var character1 : Char = 'B'
    var character2 : Char = 'D'
    var character3 : Char = 'I'

    print(character)
    print(character1)
    print(character2)
    println(character3)
//
//
//    // \t, \b, \n, \', \", \\, \$
    println("\tHello World")  // Memberikan identasi
    println("\bHello World")  // Memberikan garis baru
    println("\nHello World")  // Memberikan jarak ke bawah
    println("\'Hello World")  // Membuat tanda '
    println("\"Hello World")  // Membuat tanda"
    println("\\Hello World")  // Membuat tanda /
    println("\$Hello World")  // Membuat tanda$




}